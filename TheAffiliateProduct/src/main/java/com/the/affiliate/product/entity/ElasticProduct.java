package com.the.affiliate.product.entity;

public class ElasticProduct {
	
	private String id;
	private String affiliatewebsite;
	private String category;
	private String subcategory;
	private String size;
	private String name;
	private String origprice;
	private String offerprice;
	private String longdescription;
	private String imagename;
	private String userid;
	private String url;
	
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAffiliatewebsite() {
		return affiliatewebsite;
	}
	public void setAffiliatewebsite(String affiliatewebsite) {
		this.affiliatewebsite = affiliatewebsite;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getSubcategory() {
		return subcategory;
	}
	public void setSubcategory(String subcategory) {
		this.subcategory = subcategory;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getOrigprice() {
		return origprice;
	}
	public void setOrigprice(String origprice) {
		this.origprice = origprice;
	}
	public String getOfferprice() {
		return offerprice;
	}
	public void setOfferprice(String offerprice) {
		this.offerprice = offerprice;
	}
	public String getLongdescription() {
		return longdescription;
	}
	public void setLongdescription(String longdescription) {
		this.longdescription = longdescription;
	}
	public String getImagename() {
		return imagename;
	}
	public void setImagename(String imagename) {
		this.imagename = imagename;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	

}
